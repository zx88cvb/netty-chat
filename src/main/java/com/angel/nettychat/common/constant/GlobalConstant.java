package com.angel.nettychat.common.constant;

/**
 * 全局常量类
 */
public class GlobalConstant {
    /**
     * 是否删除
     */
    public interface IsDel {
        /**
         * 未删除
         */
        Integer NO = 0;

        /**
         * 已删除
         */
        Integer YES = 1;
    }

    /**
     * 属性
     */
    public interface Attribute {
        /**
         * 0
         */
        Integer NO = 0;

        /**
         * 1
         */
        Integer YES = 1;
    }
}
